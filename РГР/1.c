#include <locale.h>
#include <stdio.h>
// Оголошення розміру масиву через макроконстанту
#define DAYS 7

int main() {
     setlocale(LC_ALL, ""); // Встановлюємо локалізацію для чисел на "C"

    float rates[DAYS]; // Масив для збереження курсу долара на кожен день тижня
    int i; // Лічильник для циклів
    float max_rate, min_rate; // Змінні для збереження максимального та мінімального курсів
    int max_day, min_day; // Змінні для збереження днів тижня з максимальним та мінімальним курсами
    float sum = 0.0; // Змінна для збереження суми всіх курсів
    int falling = 1; // Прапорець для перевірки, чи курс весь час падав

    // Введення курсів долара на кожен день тижня
    for (i = 0; i < DAYS; i++) {
        printf("Введіть курс долара на день %d: ", i + 1); // Виводимо запрошення для введення курсу
        scanf("%f", &rates[i]); // Зчитуємо введений курс
    }

    // Ініціалізація змінних максимального та мінімального курсів
    max_rate = rates[0]; // Ініціалізуємо максимальний курс першим значенням
    min_rate = rates[0]; // Ініціалізуємо мінімальний курс першим значенням
    max_day = 0; // Ініціалізуємо день з максимальним курсом як перший день
    min_day = 0; // Ініціалізуємо день з мінімальним курсом як перший день

    // Обчислення суми, максимального та мінімального курсів та перевірка, чи курс весь час падав
    for (i = 0; i < DAYS; i++) {
        sum += rates[i]; // Додаємо курс до суми

        if (rates[i] > max_rate) { // Перевірка максимального курсу
            max_rate = rates[i]; // Оновлюємо максимальний курс
            max_day = i; // Оновлюємо день з максимальним курсом
        }

        if (rates[i] < min_rate) { // Перевірка мінімального курсу
            min_rate = rates[i]; // Оновлюємо мінімальний курс
            min_day = i; // Оновлюємо день з мінімальним курсом
        }

        if (i > 0 && rates[i] > rates[i - 1]) { // Перевірка, чи курс зростав
            falling = 0; // Якщо курс зростав, встановлюємо прапорець, що курс не падав
        }
    }

    // Виведення введених курсів
    printf("Введені курси долара:\n"); // Заголовок для виведення введених курсів
    for (i = 0; i < DAYS; i++) {
        printf("День %d: %.2f\n", i + 1, rates[i]); // Виводимо курс для кожного дня
    }

    // Виведення суми курсів
    printf("Сума курсів за тиждень: %.2f\n", sum); // Виводимо суму курсів

    // Виведення середнього значення курсу за тиждень
    float average = sum / DAYS; // Обчислюємо середнє значення курсу
    printf("Середній курс за тиждень: %.2f\n", average); // Виводимо середнє значення курсу

    // Виведення найвищого та найнижчого курсів та днів, коли вони були
    printf("Найвищий курс: %.2f на день %d\n", max_rate, max_day + 1); // Виводимо найвищий курс та день
    printf("Найнижчий курс: %.2f на день %d\n", min_rate, min_day + 1); // Виводимо найнижчий курс та день
    printf("Різниця між найвищим і найнижчим курсом: %.2f\n", max_rate - min_rate); // Виводимо різницю між найвищим та найнижчим курсами

    // Виведення повідомлення, чи курс весь час падав
    if (falling) {
        printf("Курс падав\n"); // Виводимо повідомлення, що курс падав
    } else {
        printf("Курс не падав\n"); // Виводимо повідомлення, що курс не падав
    }

    // Заміна найнижчого курсу новим значенням
    float new_rate; // Змінна для нового курсу
    printf("Введіть новий курс для заміни найнижчого курсу: "); // Запрошення для введення нового курсу
    scanf("%f", &new_rate); // Зчитування нового курсу
    rates[min_day] = new_rate; // Заміна найнижчого курсу новим значенням

    // Виведення оновлених курсів
    printf("Оновлені курси:\n"); // Заголовок для виведення оновлених курсів
    for (i = 0; i < DAYS; i++) {
        printf("День %d: %.2f\n", i + 1, rates[i]); // Виведення курсу для кожного дня
    }

    // Виведення імені студента
    printf("Програму виконав студент гр. АІ-243 Гаврилов О.В\n"); // Виводимо ім'я студента

    return 0; // Завершення програми
}                  
